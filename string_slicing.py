college="United college Prayagraj"
# basic slicing
print("college[0:6]",college[0:6]) # 0-5
print("college[7:14]",college[7:14]) # college
print("college[15:]",college[15:]) # 15 to end of string - Prayagraj
print("college[::]",college[::]) # puri string print ho jayegi
print("college[::2]",college[::2]) # alternative characters
print("college[::3]",college[::3]) # every 3rd character
print("college[::-1]",college[::-1]) # negative step (Reverse the string/reversal)